

//  Boolean values
let isLoggedIn = true;
let hasPaid = false;
console.log("isLoggedIn:", isLoggedIn, "Type:", typeof isLoggedIn);
console.log("hasPaid:", hasPaid, "Type:", typeof hasPaid);

//  Comparison operators
let a = 10;
let b = 20;
console.log("a > b:", a > b);   
console.log("a < b:", a < b);   
console.log("a == 10:", a == 10);
console.log("a != b:", a != b);  

//  Logical operators
console.log("isLoggedIn && hasPaid:", isLoggedIn && hasPaid); 
console.log("isLoggedIn || hasPaid:", isLoggedIn || hasPaid); 
console.log("!isLoggedIn:", !isLoggedIn);                     

//  Boolean conversion
console.log("Boolean(0):", Boolean(0));       
console.log("Boolean(1):", Boolean(1));       
console.log("Boolean(''):", Boolean(''));     
console.log("Boolean('Raj'):", Boolean('Raj')); 
