

//  Integer
let age = 25;
console.log("Age:", age, "Type:", typeof age);

//  Floating-point number
let price = 99.99;
console.log("Price:", price, "Type:", typeof price);

//  Negative number
let temperature = -10;
console.log("Temperature:", temperature, "Type:", typeof temperature);

//  Hexadecimal number
let hexNumber = 0xff;  
console.log("Hexadecimal Number:", hexNumber, "Type:", typeof hexNumber);

//  NaN (Not a Number)
let notANumber = "hello" / 
console.log("NaN example:", notANumber, "Type:", typeof notANumber);

// Infinity
let infinite = 1 / 0;
console.log("Infinity example:", infinite, "Type:", typeof infinite);

//  BigInt for very large integers
let bigInt = 123456789012345678901234567890n;
console.log("BigInt example:", bigInt, "Type:", typeof bigInt);

//  Number methods
let num = 123.456;
console.log("toFixed(2):", num.toFixed(2));       
console.log("toPrecision(4):", num.toPrecision(4)); 
console.log("parseInt('10'):", parseInt("10"));     
console.log("parseFloat('10.5'):", parseFloat("10.5")); 
